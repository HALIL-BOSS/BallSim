package com.example.bouncingballsim
import kotlin.math.*

// Простейший двумерный вектор с базовыми операциями.
// Используется для описания положения, скорости, сил и т.п.
data class Vec2(var x: Float, var y: Float) {

    // Сложение векторов: (x1, y1) + (x2, y2)
    operator fun plus(o: Vec2) = Vec2(x + o.x, y + o.y)

    // Вычитание векторов: (x1, y1) - (x2, y2)
    operator fun minus(o: Vec2) = Vec2(x - o.x, y - o.y)

    // Умножение вектора на скаляр: (x, y) * s
    operator fun times(s: Float) = Vec2(x * s, y * s)

    // Скалярное произведение: v · o = x1*x2 + y1*y2
    // Используется для проекций, вычисления углов и т.п.
    fun dot(o: Vec2) = x * o.x + y * o.y

    // Квадрат длины вектора: |v|^2 = x^2 + y^2
    // Вычисляется без извлечения корня для экономии.
    fun len2() = x * x + y * y

    // Длина вектора: |v| = sqrt(x^2 + y^2)
    fun len() = sqrt(len2())

    // Нормализация вектора: v / |v|
    // Если длина слишком мала, возвращается нулевой вектор,
    // чтобы избежать деления на ноль.
    fun normalized(): Vec2 {
        val l = len()
        return if (l > 1e-6f) Vec2(x / l, y / l) else Vec2(0f, 0f)
    }
}

// Ограничение значения v интервалом [a, b].
// Если v < a, возвращается a; если v > b, возвращается b.
fun clamp(v: Float, a: Float, b: Float) = max(a, min(b, v))

// Поиск ближайшей точки на отрезке AB к заданной точке P.
// Используется при проверке столкновения шара с сегментом:
// сначала находим ближайшую точку на сегменте, затем сравниваем
// расстояние до неё с радиусом шара.
fun closestPointOnSegment(p: Vec2, a: Vec2, b: Vec2): Vec2 {
    val ab = b - a
    // Квадрат длины отрезка AB, с защитой от деления на ноль
    val denom = ab.len2().coerceAtLeast(1e-6f)
    // Параметр t даёт проекцию точки P на прямую AB,
    // далее t ограничивается диапазоном [0, 1], чтобы остаться на отрезке.
    val t = clamp((p - a).dot(ab) / denom, 0f, 1f)
    // Ближайшая точка = A + AB * t
    return a + ab * t
}
